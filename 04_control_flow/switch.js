const month=1 // The below code will execute every case as we have a match at case 1
// After that every case will executed without matching the value 
// Note -> default case will also get executed without match

switch (month) {
  case 1:
    console.log("jan")
    // break;
  case 2:
    console.log("feb")
    // break;
  case 3:
    console.log("march")
    // break;
  case 4:
    console.log("april")
    // break;
  case 5:
    console.log("may")
    // break;
  case 6:
    console.log("june")
    // break;

  default:
    console.log("Your code executed successfully")
    // break;
}